#include "libft.h"

void	ft_putnbr(long long n)
{
	unsigned long long	nbr;

	if (n < 0)
	{
		n = n * -1;
		nbr = n;
		ft_putchar('-');
	}
	else
		nbr = n;
	if (nbr >= 10)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
	{
		ft_putchar(nbr + '0');
	}
}
